package com.oop.design.patterns.beahviour.command.resource;

public class SmartLight {
	
	private boolean on;
	private String color;
	public boolean isOn() {
		return on;
	}
	public void setOn(boolean on) {
		this.on = on;
	}
	public String getColor() {
		return color;
	}
	public void setColor(String color) {
		this.color = color;
	}
	
	

}
